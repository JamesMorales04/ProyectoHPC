int distanciaMinima(int distancia[], bool sptSet[])
{
	int min = INT_MAX, min_index;

	for (int v = 0; v < Tamaño; v++) {

		if (sptSet[v] == false && distancia[v] <= min) {

			min = distancia[v];
			min_index = v;

		}

	}

	return min_index;
}



